    private void BtnPacientes4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnPacientes4ActionPerformed

        int selectedRow = TabelaListaPacientes.getSelectedRow();
        if (selectedRow != -1)
        {
            pacienteDAO pacienteDAO = new pacienteDAO();
            paciente pacienteSelecionado = pacienteDAO.listar().get(selectedRow);
            String data = TxtData.getText(); SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
            Date date = null;
            try { date = dateFormat.parse(data);
                System.out.println("Data convertida: " + date);
            } catch (ParseException e)
            { e.printStackTrace();
            }
            if (date != null)
            { consulta consulta = new consulta(pacienteSelecionado.getNome(), date);
                consulta.setReceitaObservacoes(TxtReceitaObs.getText());
                consulta.setConsultaRealizada(check_consulta_realizada.isSelected());
                consulta.setConsultaPaga(Check_Pago.isSelected());
                consulta.setValor(TxtValor.getText());
                consultaDAO consultaDAO = new consultaDAO();
                consultaDAO.cadastrar(consulta);
                 JOptionPane.showMessageDialog(this, "cadastro realizado com sucesso.");
            dispose();
            } else
            { JOptionPane.showMessageDialog(this, "Data inválida."); }
        } else { JOptionPane.showMessageDialog(this, "Selecione um paciente.");
        }

    }//GEN-LAST:event_BtnPacientes4ActionPerformed

